    <h1>Perang Hamas Vs Israel: Situasi Memburuk - Muncul 'Kota Hantu'</h1>

    <img src="<?php echo base_url() ?>/assets/img/gaza.jpeg" alt="Foto : REUTERS/MOHAMMED SALEM">

    <br>
    <p> <h4>Jakarta, CNBC Indonesia</h4> Perang antara Hamas dan Israel kian memanas. Serangan mendadak salah satu faksi Palestina tersebut pada akhir pekan lalu dibalas dengan gempuran mematikan ke wilayah Gaza.
    Israel menggempur sasaran Hamas di Jalur Gaza selama lima hari berturut-turut sejak serangan berani militan tersebut dan jumlah korban tewas meningkat hingga ribuan, ketika Perdana Menteri Benjamin Netanyahu bersumpah akan menghancurkan mereka sepenuhnya.
    "Setiap anggota Hamas adalah orang mati," kata pemimpin veteran sayap kanan Israel itu, sekali lagi menyamakan mereka dengan kelompok ISIS. "Kami akan menghancurkan mereka dan menghancurkan mereka sebagaimana dunia telah menghancurkan Daesh," katanya, dilansir AFP, Kamis (12/10/2023).
    <br>
    <br>
    Netanyahu sebelumnya untuk sementara menyelesaikan perbedaan politiknya dan membentuk pemerintahan darurat, termasuk dengan mantan menteri pertahanan berhaluan tengah Benny Gantz selama krisis terjadi.
    <br>
    <br>
    Menurut militer Israel, serangan mendadak pada Sabtu - yang terburuk dalam 75 tahun sejarah Israel - telah menyebabkan total 1.200 orang Israel tewas yang kebanyakan dari mereka adalah warga sipil.
    <br>
    <br>
    Di Gaza, para pejabat melaporkan lebih dari 1.000 orang tewas dalam serangan udara dan artileri yang terus menerus dilakukan Israel di daerah kantong Palestina yang padat penduduk, sehingga menimbulkan asap hitam yang membumbung ke langit dan meratakan seluruh blok kota.
    <br>
    <br>
    PBB mengatakan 11 stafnya tewas di Gaza sejak Sabtu, sementara Palang Merah Internasional dan Bulan Sabit Merah mengatakan mereka kehilangan lima anggotanya.
    <br>
    <br>
    Di Tepi Barat yang diduduki, setidaknya empat warga Palestina tewas ketika pemukim Israel bersenjata menyerang sebuah kota di selatan Nablus, sehingga jumlah korban tewas menjadi 29 orang.
    <br>    
    <br>    
    Israel mengerahkan pasukan, tank, dan kendaraan lapis baja berat lainnya di sekitar Gaza dalam operasi pembalasan terhadap apa yang disebut Netanyahu sebagai "serangan yang kebiadabannya... belum pernah kita lihat sejak Holocaust".
    <br>
    <br>
    Sementara itu, Presiden AS Joe Biden berjanji untuk mengirim lebih banyak amunisi dan perangkat keras militer ke sekutu dekatnya, Israel, dan menyatakan rasa jijiknya atas "kejahatan" pembantaian warga sipil dalam serangan Hamas yang belum pernah terjadi sebelumnya.
    <br>
    <br>
    Krisis yang dijuluki "Israel 9/11" ini membuat Netanyahu mencapai kesepakatan politik dengan Gantz dan berjanji untuk membekukan rencana perombakan peradilan pemerintahannya yang telah memicu protes massal yang belum pernah terjadi sebelumnya.
    <br>
    <br>
    Pemimpin oposisi Yair Lapid belum bergabung dengan aliansi sementara tersebut, meskipun pernyataan bersama mengatakan satu kursi akan "disediakan" untuknya di kabinet perang.
    <br>
    <br>
    "Israel adalah yang utama," tulis Gantz dalam unggahan media sosialnya, sementara Menteri Keamanan Nasional sayap kanan Itamar Ben-Gvir menulis bahwa dia "menyambut persatuan, sekarang kita harus menang".
    </p>

    <h3>Ketakutan Sandera</h3>
    <p>Ketika perang berkecamuk, kekhawatiran meningkat di Israel atas nasib setidaknya 150 sandera, termasuk warga asing dan berkewarganegaraan ganda, yang ditahan di Gaza oleh Hamas.
    <br>
    <br>
    Kelompok tersebut mengeklaim bahwa empat tawanan tewas dalam serangan Israel dan mengancam akan membunuh sandera lainnya jika sasaran sipil dibom tanpa peringatan terlebih dahulu dari Israel.
    <br>
    <br>
    Kekhawatiran meningkat atas memburuknya krisis kemanusiaan di Gaza yang dilanda perang, di mana Israel telah meratakan lebih dari 1.000 bangunan dan memberlakukan pengepungan total, memutus pasokan air, makanan dan energi untuk 2,3 juta orang.
    <br>
    <br>
    Satu-satunya pembangkit listrik di wilayah kantong tersebut ditutup pada Rabu setelah kehabisan bahan bakar.
    <br>
    <br>
    Lebih dari 260.000 warga Gaza terpaksa meninggalkan rumah mereka, kata badan bantuan PBB, dan Sekretaris Jenderal badan dunia tersebut, Antonio Guterres, menyuarakan kekhawatiran akan memburuknya situasi kemanusiaan yang sudah mengerikan.
    <br>
    <br>
    Uni Eropa menyerukan "koridor kemanusiaan" yang memungkinkan warga sipil melarikan diri dari perang kelima di wilayah kantong tersebut dalam 15 tahun terakhir.
    <br>
    <br>
    Pertemuan para menteri luar negeri Liga Arab di Kairo juga menyerukan agar bantuan diizinkan masuk ke Gaza "segera".
    </p>

    <h3>Ancaman Serangan</h3>
    <p>Di sisi lain, Israel tampaknya bersiap untuk kemungkinan invasi darat ke Gaza, namun menghadapi ancaman perang multi-front setelah juga mendapat serangan roket dari kelompok militan di negara tetangga Lebanon dan Suriah.
    <br>
    <br>
    Israel kembali menyerang sasaran pada Rabu di Lebanon selatan, sebuah wilayah yang dikuasai oleh Hizbullah yang didukung Iran.
    <br>
    <br>
    Pada Rabu malam, sirene roket terdengar di utara Israel, dan tentara mengatakan ada dugaan "infiltrasi" udara dari Lebanon. Ia kemudian mundur, menyalahkan "kesalahan".
    <br>
    <br>
    Biden, yang telah mengalihkan kelompok tempur kapal induk ke Mediterania timur, telah memperingatkan musuh-musuh Israel - negara atau kelompok - untuk tidak terlibat.
    <br>
    <br>
    Pesawat Amerika pertama telah mengirimkan "persenjataan canggih" ke Pangkalan Udara Nevatim di Israel selatan, kata tentara Israel.
    </p>

    <h3>Jumlah Korban</h3>
    <p>Israel sangat terguncang oleh serangan paling mematikan sejak pembentukannya pada 1948 dan kegagalan intelijen yang memungkinkan lebih dari 1.500 militan menerobos penghalang keamanan Gaza dalam serangan darat, udara, dan laut yang terkoordinasi pada hari Sabat Yahudi.
    <br>
    <br>
    Departemen Luar Negeri AS mengatakan setidaknya 22 warga AS telah terbunuh, ketika Menteri Luar Negeri Antony Blinken menuju ke Israel untuk menunjukkan solidaritas.
    <br>
    <br>
    Pasukan Israel telah merebut kembali lebih dari selusin kota di selatan dekat Gaza setelah berhari-hari pertempuran jalanan yang melelahkan yang menyebabkan sedikitnya 1.500 militan Hamas 'berserakan' di jalanan.
    <br>
    <br>
    Juru bicara militer Jonathan Conricus mengatakan mereka menemukan "1.200 warga Israel tewas", sebagian besar warga sipil, saat mereka pergi dari rumah ke rumah. Adapun tentara Israel kemudian melaporkan 169 personelnya gugur.
    <br>
    <br>
    Pasukan telah menghadapi dan membunuh beberapa militan Hamas yang bertahan, kata juru bicara militer Israel Daniel Hagari, termasuk 18 orang pada Rabu.
    <br>
    <br>
    Adapun tentara Israel telah memanggil 300.000 tentara cadangan untuk apa yang Netanyahu katakan akan menjadi perang yang "panjang dan sulit".
    <br>
    <br>
    Menteri Intelijen Israel Gila Gamliel mengatakan kepada AFP bahwa perang untuk "mencabut" Hamas akan menghalangi militan melakukan serangan di seluruh dunia.
    </p>
 
    <h3>'Kota Hantu'</h3>
    <p>Pengeboman besar-besaran kembali menghujani Gaza, di mana langit menjadi hitam dan Hamas mengatakan sedikitnya 30 orang tewas dalam serangan semalam.
    <br>
    <br>
    Puing-puing, mobil-mobil yang terbakar, dan pecahan kaca menutupi jalan-jalan di Kota Gaza, tempat bom menghantam Universitas Islam yang terkait dengan Hamas.
    <br>
    <br>
    Yang juga menjadi sasaran adalah bangunan tempat tinggal, masjid, pabrik dan toko, kata Salama Marouf dari kantor media pemerintah Gaza.
    <br>
    <br>
    Warga Gaza, Mazen Mohammad (38) mengatakan keluarganya yang ketakutan menghabiskan malam itu dengan berkumpul bersama ketika ledakan mengguncang daerah tersebut, sebelum muncul di pagi hari untuk menilai kehancuran total di lingkungan mereka.
    <br>
    <br>
    "Kami merasa seperti berada di kota hantu, seolah-olah hanya kami yang selamat," kata Mohammad kepada AFP.
    <br>
    <br>
    Persediaan medis, termasuk oksigen, semakin menipis di rumah sakit Al-Shifa di Gaza yang kewalahan, kata dokter ruang gawat darurat Mohammed Ghonim.
    </p>

    <h3>Ketakutan dan Ketidakpercayaan</h3>
    <p>Kerusuhan telah berkobar di Tepi Barat yang diduduki, tempat protes diadakan sebagai bentuk solidaritas terhadap Gaza dan 27 warga Palestina tewas dalam bentrokan sejak Sabtu.
    <br>
    <br>
    Kota-kota di Israel sangat sepi dan tegang, dengan beberapa penduduk merasakan meningkatnya rasa takut dan ketidakpercayaan antara orang Yahudi dan anggota minoritas Arab-Israel.
    <br>
    <br>
    Diplomasi yang intens terus berlanjut ketika kekuatan internasional dan regional berupaya mencegah konflik yang lebih luas di Timur Tengah.
    </p>
